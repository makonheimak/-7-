<?php
echo "<h2>Задание 7: Города, начинающиеся с буквы К</h2>";

$regions = [
    'Московская область' => ['Москва', 'Зеленоград', 'Клин'],
    'Ленинградская область' => ['Санкт-Петербург', 'Всеволожск', 'Павловск', 'Кронштадт'],
    'Рязанская область' => ['Рязань', 'Касимов', 'Скопин', 'Сасово'],
    'Краснодарский край' => ['Краснодар', 'Кореновск', 'Курганинск', 'Сочи']
];

foreach ($regions as $region => $cities) {
    $filteredCities = [];
    foreach ($cities as $city) {
        if (mb_substr($city, 0, 1) === 'К') {
            $filteredCities[] = $city;
        }
    }
    if (count($filteredCities) > 0) {
        echo $region . ": " . implode(', ', $filteredCities) . "<br>";
    }
}
?>
